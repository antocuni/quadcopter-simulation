def attitudeControl(quad, time):
    """
    Original logic: use the logic in controller.py to follow the trajectory
    generated by trajGen
    """
    desired_state = trajGen.genLine(time[0])
    F, M = controller.run(quad, desired_state)
    quad.update(dt, F, M)
    time[0] += dt

